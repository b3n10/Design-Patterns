package com.hometheater;

public class PopcornPopper {
    void on() {
        System.out.println("Popcorn popper turning on..");
    }

    void pop() {
        System.out.println("Popcorn popper now popping..");
    }

    public void off() {
        System.out.println("Turning off popper..");
    }
}
